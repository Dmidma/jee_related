    @ManyToMany(mappedBy = "member1Collection")
    private Collection<Team> teamCollection;
    @OneToOne(cascade = CascadeType.ALL, mappedBy = "member1")
    private MemberRole memberRole;
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "idMember")
    private Collection<Comment> commentCollection;
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "idMember")
    private Collection<MemberTicket> memberTicketCollection;

    public Member1() {
    }

    public Member1(Integer id) {
        this.id = id;
    }

    public Member1(Integer id, String name, String lastName, String email, String job) {
        this.id = id;
        this.name = name;
        this.lastName = lastName;
        this.email = email;
        this.job = job;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    @XmlTransient
    public Collection<Team> getTeamCollection() {
        return teamCollection;
    }

    public void setTeamCollection(Collection<Team> teamCollection) {
        this.teamCollection = teamCollection;
    }

    public MemberRole getMemberRole() {
        return memberRole;
    }

    public void setMemberRole(MemberRole memberRole) {
        this.memberRole = memberRole;
    }

    @XmlTransient
    public Collection<Comment> getCommentCollection() {
        return commentCollection;
    }

    public void setCommentCollection(Collection<Comment> commentCollection) {
        this.commentCollection = commentCollection;
    }

    @XmlTransient
    public Collection<MemberTicket> getMemberTicketCollection() {
        return memberTicketCollection;
    }

    public void setMemberTicketCollection(Collection<MemberTicket> memberTicketCollection) {
        this.memberTicketCollection = memberTicketCollection;
    }

